package first;

public class TypeCasting 
{
	//casting ==> explicitly converting data types (int)12.000
	//implicit casting ==> converting smaller data types (ex:int) to larger data types (ex: double)
	double num = 12;
	//explicit casting ==> converting larger data types (ex:double) to smaller data types (ex:int)
	int num1 = (int)10.00;
}
